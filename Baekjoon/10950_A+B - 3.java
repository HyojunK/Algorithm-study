import java.util.*;

public class Main {

	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		int count = 0;
		int a, b = 0;
		
		count = scan.nextInt();
		for(int i = 0; i < count; i++) {
		a = scan.nextInt();
		b = scan.nextInt();
		System.out.println(a + b);
		
		}
	}
}
